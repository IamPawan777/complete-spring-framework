package com.spring;

public class Student {
	private int id;
	private String name;
	
	public int getId() {
		return id;
	}
	
	public Student() {
		super();
	}
	
	public Student(int id, String name) {
		super();
		this.id = id;
		this.name = name;
	}

	@Override
	public String toString() {
		return "Student : id is= " + id + " || name is= " + name;
	}
	
}
